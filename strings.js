const escola = "Cod3r"

console.log(escola.charAt(4)) //o charAt serve para mostrar a letra na posição 4

console.log(escola.indexOf('3')) // serve para verificar qual letra esta na posição 3

console.log(escola.substring(1)) // serve para iniciar a palavra a partir da letra que esta na posição 1

console.log(escola.substring(0, 3)) // serve para começar a palavra pela letra na posição 0 até a posição 3

console.log('Escola '.concat(escola).concat("!")) // serve para concatenar as strings

console.log('Escola '+ escola + "!") // esse modo tb serve para concatenar

console.log(escola.replace(3, 'e')) //serve para alterar a palavra que esta na posição 3 para a letra que foi declarada

console.log('Ana,Maria,Pedro'.split(',')) // o comando split serve para separar em uma array os elementros da string e separalos por virgula

